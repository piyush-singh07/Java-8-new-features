package com.demo.treeset.examples;

import java.util.TreeSet;

public class TreeSetWithDNSO {

	public static void main(String[] args) {
		
		TreeSet<String> tstr= new TreeSet<>();
		tstr.add("Hello");
		tstr.add("Bye");
		tstr.add("Hi");
		tstr.add("Going");
		
		System.out.println(tstr);
		
		
		TreeSet<Integer> ti = new TreeSet<Integer>();
		ti.add(10);
		ti.add(20);
		ti.add(2);
		ti.add(90);
		
		System.out.println(ti);
		
		
		
		
		
		
	}

}
